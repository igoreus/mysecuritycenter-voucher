<?php
/**
 * Generator interface.
 */


interface GeneratorInterface {

    /**
     * Setter for quantity.
     *
     * @param int $quantity
     * @return mixed
     */
    public function setQuantity($quantity);

    /**
     * Setter for position.
     *
     * @param $position
     * @return mixed
     */
    public function setPosition($position);

}